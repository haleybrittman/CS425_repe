CREATE SCHEMA `reserves` ;
CREATE TABLE `reserves`.`reserves` (
  `SID` INT NOT NULL,
  `BID` INT NOT NULL,
  `Day` DATE NULL,
  `Deposit` DECIMAL(10,2) NULL,
  PRIMARY KEY (`SID`, `BID`),
  CONSTRAINT `SID`
    FOREIGN KEY (`SID`)
    REFERENCES `reserves`.`sailors` (`SID`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  CONSTRAINT `BID`
    FOREIGN KEY (`BID`)
    REFERENCES `reserves`.`boats` (`BID`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION);

